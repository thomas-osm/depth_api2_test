package net.sf.seesea.data.io;

public class WriterException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -4891396908469849616L;

	public WriterException() {
		super();
	}

	public WriterException(String arg0, Throwable arg1, boolean arg2,
			boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public WriterException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public WriterException(String arg0) {
		super(arg0);
	}

	public WriterException(Throwable arg0) {
		super(arg0);
	}

	
}
