package net.sf.seesea.data.postprocessing.process;

public class StatisticsException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1920317189735037038L;

	public StatisticsException() {
	}

	public StatisticsException(String arg0) {
		super(arg0);
	}

	public StatisticsException(Throwable arg0) {
		super(arg0);
	}

	public StatisticsException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public StatisticsException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
