package net.sf.seesea.gauge;

public class GaugeUpdateException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -5112871536324040978L;

	public GaugeUpdateException() {
		super();
	}

	public GaugeUpdateException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public GaugeUpdateException(String message, Throwable cause) {
		super(message, cause);
	}

	public GaugeUpdateException(String message) {
		super(message);
	}

	public GaugeUpdateException(Throwable cause) {
		super(cause);
	}

	
	
}
