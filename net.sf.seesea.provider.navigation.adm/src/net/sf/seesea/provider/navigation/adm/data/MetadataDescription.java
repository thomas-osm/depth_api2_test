package net.sf.seesea.provider.navigation.adm.data;

public class MetadataDescription {

	private final short id;
	private final short size;

	public MetadataDescription(short id, short size) {
		this.id = id;
		this.size = size;
	}

	public short getId() {
		return id;
	}

	public short getSize() {
		return size;
	}
	
	
	
}
