package net.sf.seesea.content.api;

public class ContentDetectionException extends Exception {

	public ContentDetectionException() {
		// 
	}

	public ContentDetectionException(String arg0) {
		super(arg0);
	}

	public ContentDetectionException(Throwable arg0) {
		super(arg0);
	}

	public ContentDetectionException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ContentDetectionException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
