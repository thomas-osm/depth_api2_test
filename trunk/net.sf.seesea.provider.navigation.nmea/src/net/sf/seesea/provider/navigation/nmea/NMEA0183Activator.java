package net.sf.seesea.provider.navigation.nmea;

import org.osgi.framework.BundleActivator;
import org.osgi.framework.BundleContext;

/**
 * The activator class controls the plug-in life cycle
 */
public class NMEA0183Activator implements BundleActivator {

	// The plug-in ID
	public static final String PLUGIN_ID = "net.sf.seesea.provider.navigation.nmea"; //$NON-NLS-1$

	private static BundleContext context;
	
	/**
	 * The constructor
	 */
	public NMEA0183Activator() {
	}

	/*
	 * (non-Javadoc)
	 * @see org.eclipse.ui.plugin.AbstractUIPlugin#start(org.osgi.framework.BundleContext)
	 */
	@Override
	public void start(BundleContext context) throws Exception {
		this.context = context;
	}

	/*
	 * (non-Javadoc)
	 * @see org.eclipse.ui.plugin.AbstractUIPlugin#stop(org.osgi.framework.BundleContext)
	 */
	@Override
	public void stop(BundleContext context) throws Exception {
		this.context = null;
	}

	public static BundleContext getContext() {
		return context;
	}

	

}
