package net.sf.seesea.track.api.exception;

public class TrackPerssitenceException extends Exception {

	public TrackPerssitenceException() {
	}

	public TrackPerssitenceException(String arg0) {
		super(arg0);
	}

	public TrackPerssitenceException(Throwable arg0) {
		super(arg0);
	}

	public TrackPerssitenceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public TrackPerssitenceException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
